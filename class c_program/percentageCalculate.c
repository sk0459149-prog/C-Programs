//Calculating percentage of 5 subjects
#include<stdio.h>
int main(){
    float x = 100; // maths marks
    float y =80; // physics marks
    float z =90; // english marks
    float a =87; // chemistry marks
    float b =70; //computer marks
    float t=(x+y+z+a+b)/5;
    printf("The percentage of five subjects is: %lf",t );

    return 0;
}